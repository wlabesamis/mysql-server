CREATE DATABASE IF NOT EXISTS kitra_db;

USE kitra_db;

CREATE TABLE users (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(50),
    age INT(3),
    password VARCHAR(50),
    email VARCHAR(255)
);

CREATE TABLE treasures (
    id INT PRIMARY KEY AUTO_INCREMENT,
    latitude DECIMAL(16, 13) NOT NULL,
    longitude DECIMAL(16, 13) NOT NULL,
    Name VARCHAR(255) NOT NULL
);

CREATE TABLE money_values (
    treasure_id INT,
    amt INT,
    FOREIGN KEY (treasure_id) REFERENCES treasures(Id)
);

